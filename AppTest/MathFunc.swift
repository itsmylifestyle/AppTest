import Foundation


class mathFunc {
    func addInteger(x: Int, y: Int) -> Int {
        return x + y
    }
    
    func multInteger(x: Int, y: Int) -> Int {
        return x * y
    }
    
    func divInteger(x: Int, y: Int) -> Int {
        return x / y
    }
    
    func minInteger(x: Int, y: Int) -> Int {
        return x - y
    }
}



